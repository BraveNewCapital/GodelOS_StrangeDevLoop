"""
RecursiveSelfObserver: Bounded recursive self-observation with VAE compression.

This module implements the post-Protocol Theta bounded recursive self-observer
that simulates the recursive dynamics, computes contraction φ(s) = W s + b with
spectral radius ρ(W) < 1, approximates integrated information φ_n via latent
mutual information across steps, computes surprise p_n using a simple
autoregressive predictor over latent z_t, and derives a consciousness metric
C_n along with a FocusOn(...) attention directive.

Mathematical specifications implemented:

1) Bounded recursion:
   Λ[S_t] = α S_t + (1 - α) Λ[S_{t-1}] + η_t
   with α ∈ [0, 1], η_t ~ N(0, σ^2 I), and bounded steps n ≤ 10.

2) Contraction mapping:
   φ(s) = W s + b with spectral radius ρ(W) < 1 enforced via spectral scaling.
   We provide a scalar φ_s used by the utility function as:
     φ_s = ||φ(s)||_2 / (||s||_2 + ε)
   where ε is a small constant to avoid division by zero.

3) Integrated Information (φ_n):
   φ_n = φ_{n-1} + I(z_t; z_{t-1})
   where I is approximated using the cosine similarity between consecutive latents:
     I(z_t; z_{t-1}) ≈ max(0, cos_sim(z_t, z_{t-1}))
   and φ_n is bounded above by log(k), with k = input_dim.

4) Surprise (p_n):
   p_n is computed using a simple linear AR predictor for z_{t} from z_{t-1}.
   We solve a ridge-regularized least-squares estimate of A in:
     z_t ≈ z_{t-1} A
   and compute a Gaussian negative log-likelihood (NLL) of residuals over
   a sliding window. p_n is the average NLL per-step.

5) Consciousness (C_n):
   C_n = 1 / (1 + exp(- (log(1 + φ_n) + p_n - 0.5)))
   with gn = 1 and rn = 1 implicitly.

6) FocusOn predicate:
   If p_n > 0.5, return FocusOn("visual", "anomaly", 0.9),
   else FocusOn("none", "baseline", 0.1).

Integration:
- This component depends on a trained VAECompressor to produce 128-D latent z_t
  from 512-D recursive states Λ[S_t]. It does not trigger external LLM calls.
- Designed to integrate into Protocol Theta self-preservation pipeline.

Usage example (simplified):
    vae = VAECompressor()
    vae.train_on_synthetic()
    observer = RecursiveSelfObserver(vae=vae, recursion_depth=10, alpha=0.8, sigma=0.1)

    s = np.random.randn(512).astype(np.float32)
    out = observer.step(s)
    print(out["phi_n"], out["p_n"], out["C_n"], out["focus"])

Notes:
- All computations are CPU-friendly and rely on numpy and PyTorch.
- This class is deterministic given a fixed seed and identical inputs.

"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Dict, Optional, Tuple, List

import numpy as np
import torch

# Local import (expected to be available in the same package)
# from .vae_compressor import VAECompressor  # Documented reference; not imported here to avoid circular hints/types.


@dataclass(frozen=True)
class FocusOn:
    """Attention directive generated by the self-observer.

    Attributes:
        channel: High-level sensory/cognitive channel to emphasize (e.g., "visual").
        region: Region or type of content to prioritize (e.g., "anomaly").
        priority: Priority value in [0, 1], where higher means stronger emphasis.
    """
    channel: str
    region: str
    priority: float


class RecursiveSelfObserver:
    """Simulates bounded recursive self-observation and computes consciousness metrics.

    This class encapsulates:
    - Recursive update Λ[S_t] with parameters α and σ.
    - Contraction mapping φ(s) = W s + b with ρ(W) < 1.
    - VAE compression of Λ[S_t] into a 128-D latent z_t.
    - Integrated information approximation φ_n via cosine similarity of z-sequence.
    - Surprise p_n via linear AR prediction over z-sequence.
    - Consciousness metric C_n and FocusOn attention directive.

    Parameters:
        vae: Trained VAE-like compressor exposing compress(x, use_mean=True) -> np.ndarray
             with input shape [N, 512] and output shape [N, 128].
        input_dim: Dimensionality of the observed state S_t (default 512).
        latent_dim: Dimensionality of latent z_t (default 128).
        recursion_depth: Maximum number of recursion steps n (bounded to ≤ 10).
        alpha: Recursion smoothing coefficient α in [0, 1].
        sigma: Standard deviation σ for Gaussian noise η_t.
        ridge_lambda: Ridge coefficient λ for AR estimation stability (default 1e-3).
        ar_window: Sliding window size for AR estimation and p_n computation (default 20).
        seed: Optional random seed for deterministic behavior.

    Raises:
        ValueError: If invalid hyperparameters are provided.
    """

    def __init__(
        self,
        vae,
        input_dim: int = 512,
        latent_dim: int = 128,
        recursion_depth: int = 10,
        alpha: float = 0.8,
        sigma: float = 0.1,
        ridge_lambda: float = 1e-3,
        ar_window: int = 20,
        seed: Optional[int] = 42,
    ) -> None:
        if input_dim <= 0 or latent_dim <= 0:
            raise ValueError("input_dim and latent_dim must be positive.")
        if recursion_depth < 1 or recursion_depth > 10:
            raise ValueError("recursion_depth must be in [1, 10].")
        if not (0.0 <= alpha <= 1.0):
            raise ValueError("alpha must be in [0, 1].")
        if sigma <= 0.0:
            raise ValueError("sigma must be > 0.0.")
        if ar_window < 2:
            raise ValueError("ar_window must be ≥ 2.")

        self.vae = vae
        self.input_dim = int(input_dim)
        self.latent_dim = int(latent_dim)
        self.recursion_depth = int(recursion_depth)
        self.alpha = float(alpha)
        self.sigma = float(sigma)
        self.ridge_lambda = float(ridge_lambda)
        self.ar_window = int(ar_window)
        self.seed = seed

        # Random seeds for deterministic behavior if provided
        if self.seed is not None:
            np.random.seed(self.seed)
            torch.manual_seed(self.seed)

        # Contraction mapping parameters (W, b) with ρ(W) < 1
        self.W, self.b = self._init_contraction(self.input_dim, spectral_target=0.9)

        # Recursion running state and history buffers
        self._Lambda_prev = np.zeros(self.input_dim, dtype=np.float32)
        self._step_count = 0

        # Metrics state
        self.log_k = float(np.log(self.input_dim))
        self.phi_n = 0.0  # Integrated information accumulator
        self.p_n = 0.0    # Surprise estimate
        self.C_n = 0.0    # Consciousness metric

        # Latent history buffers (for AR and MI)
        self._z_hist: List[np.ndarray] = []  # list of shape [latent_dim,]
        self._phi_scalar_last = 0.0

    # -------------------------------------------------------------------------
    # Initialization helpers
    # -------------------------------------------------------------------------

    def _init_contraction(self, dim: int, spectral_target: float = 0.9) -> Tuple[np.ndarray, np.ndarray]:
        """Initialize contraction matrix W and bias b with spectral radius < 1.

        We create a random matrix W, compute its spectral radius ρ(W), and scale
        it so that ρ(W_scaled) = spectral_target < 1, ensuring contraction.

        Args:
            dim: Dimensionality for square W ∈ R^{dim x dim}.
            spectral_target: Target spectral radius after scaling (< 1).

        Returns:
            Tuple (W, b) where:
                - W: np.ndarray of shape [dim, dim] with ρ(W) < 1
                - b: np.ndarray of shape [dim,] small random bias

        """
        W = np.random.randn(dim, dim).astype(np.float32) / np.sqrt(dim)
        rho = self._spectral_radius(W)
        if rho <= 0:
            rho = 1.0
        scale = spectral_target / rho
        W = W * scale
        # Verify spectral radius
        rho_new = self._spectral_radius(W)
        if rho_new >= 1.0:
            # Fallback: enforce extra margin
            W = W * (0.9 / (rho_new + 1e-8))
        b = (0.01 * np.random.randn(dim)).astype(np.float32)
        return W, b

    @staticmethod
    def _spectral_radius(W: np.ndarray) -> float:
        """Compute spectral radius (largest absolute eigenvalue) of a square matrix.

        Args:
            W: Square matrix.

        Returns:
            Spectral radius ρ(W).

        Notes:
            For performance on large matrices, we approximate using a few power iterations.
        """
        # Power iteration for spectral norm approximation (upper bound for ρ(W))
        v = np.random.randn(W.shape[0]).astype(np.float32)
        v = v / (np.linalg.norm(v) + 1e-8)
        for _ in range(15):
            v = W @ v
            norm_v = np.linalg.norm(v) + 1e-8
            v = v / norm_v
        # Rayleigh quotient approximation
        num = float(v @ (W @ v))
        den = float(v @ v) + 1e-8
        # Absolute value to approximate spectral radius; robustify with matrix-2 norm as well
        approx_rq = abs(num / den)
        # Also compute matrix-2 norm upper bound via np.linalg.norm(W, 2) if feasible
        try:
            norm2 = float(np.linalg.norm(W, 2))
            return min(max(approx_rq, 1e-8), norm2 + 1e-8)
        except Exception:
            return max(approx_rq, 1e-8)

    # -------------------------------------------------------------------------
    # Core computations
    # -------------------------------------------------------------------------

    def _recursion_update(self, s_t: np.ndarray) -> np.ndarray:
        """Compute Λ[S_t] = α S_t + (1 - α) Λ[S_{t-1}] + η_t.

        Args:
            s_t: Current observed state vector in R^{input_dim}.

        Returns:
            Lambda_t: Updated recursive state Λ[S_t].

        Raises:
            ValueError: If s_t has incorrect shape.
        """
        if s_t.shape != (self.input_dim,):
            raise ValueError(f"s_t must have shape ({self.input_dim},), got {s_t.shape}")

        eta = np.random.randn(self.input_dim).astype(np.float32) * self.sigma
        Lambda_t = (self.alpha * s_t) + ((1.0 - self.alpha) * self._Lambda_prev) + eta
        self._Lambda_prev = Lambda_t.astype(np.float32)
        self._step_count += 1
        return self._Lambda_prev

    def _contraction_scalar(self, s: np.ndarray) -> Tuple[np.ndarray, float]:
        """Apply contraction mapping φ(s) = W s + b and return scalar φ_s.

        The scalar φ_s is defined as:
            φ_s = ||φ(s)||_2 / (||s||_2 + ε)

        Args:
            s: State vector input.

        Returns:
            Tuple (phi_vec, phi_scalar) where:
                - phi_vec: The contracted vector φ(s).
                - phi_scalar: The scalar used by the self-preservation utility.
        """
        phi_vec = (self.W @ s) + self.b
        denom = float(np.linalg.norm(s) + 1e-8)
        phi_scalar = float(np.linalg.norm(phi_vec) / denom)
        return phi_vec.astype(np.float32), phi_scalar

    @staticmethod
    def _cosine_similarity(a: np.ndarray, b: np.ndarray) -> float:
        """Compute cosine similarity between two vectors."""
        denom = (np.linalg.norm(a) * np.linalg.norm(b)) + 1e-8
        return float(np.dot(a, b) / denom)

    def _update_phi_n(self, z_t: np.ndarray) -> float:
        """Update integrated information accumulator φ_n using z_t and history.

        φ_n = φ_{n-1} + I(z_t; z_{t-1}), with:
            I(z_t; z_{t-1}) ≈ max(0, cos_sim(z_t, z_{t-1}))

        The accumulator is bounded by log(k) with k = input_dim.

        Args:
            z_t: Current latent vector in R^{latent_dim}.

        Returns:
            Updated φ_n.
        """
        if self._z_hist:
            z_prev = self._z_hist[-1]
            cos_sim = self._cosine_similarity(z_t, z_prev)
            incr = max(0.0, cos_sim)
            self.phi_n = min(self.log_k, self.phi_n + incr)
        else:
            # First latent does not update φ_n
            self.phi_n = min(self.log_k, self.phi_n)
        return self.phi_n

    def _estimate_ar_A(self, X: np.ndarray, Y: np.ndarray) -> np.ndarray:
        """Estimate linear predictor A in Y ≈ X A via ridge-regularized least squares.

        Args:
            X: Matrix of shape [T, L] with predictors (z_{t-1}).
            Y: Matrix of shape [T, L] with targets (z_t).

        Returns:
            A: Estimated coefficient matrix of shape [L, L].
        """
        # Solve A = (X^T X + λ I)^{-1} X^T Y
        L = X.shape[1]
        XtX = X.T @ X
        XtY = X.T @ Y
        ridge = self.ridge_lambda * np.eye(L, dtype=np.float32)
        try:
            A = np.linalg.solve(XtX + ridge, XtY)  # [L, L]
        except np.linalg.LinAlgError:
            A = np.linalg.pinv(XtX + ridge) @ XtY
        return A.astype(np.float32)

    def _compute_surprise(self) -> float:
        """Compute p_n using an AR predictor over the recent z-history.

        We build pairs (z_{t-1}, z_t) over the last ar_window steps, estimate
        a linear predictor A: z_t ≈ z_{t-1} A, and compute the Gaussian NLL
        of residuals. The reported p_n is the mean per-step NLL across the window.

        Returns:
            Surprise p_n (non-negative).
        """
        T = len(self._z_hist)
        if T < 2:
            self.p_n = 0.0
            return self.p_n

        # Construct windowed data
        start = max(1, T - self.ar_window)
        X_list = []
        Y_list = []
        for t in range(start, T):
            X_list.append(self._z_hist[t - 1])
            Y_list.append(self._z_hist[t])
        X = np.stack(X_list, axis=0)  # [T-1_w, L]
        Y = np.stack(Y_list, axis=0)  # [T-1_w, L]

        # Estimate A and compute residuals
        A = self._estimate_ar_A(X, Y)  # [L, L]
        Y_hat = X @ A                  # [T-1_w, L]
        R = Y - Y_hat                  # residuals

        # Estimate isotropic variance σ^2 from residuals
        rss = float(np.sum(R**2))
        D = float(R.shape[0] * R.shape[1]) + 1e-8
        sigma2 = max(rss / D, 1e-8)

        # Gaussian NLL for residuals: 0.5 * [d log(2πσ^2) + ||r||^2 / σ^2]
        const_term = 0.5 * (R.shape[1] * np.log(2.0 * np.pi * sigma2))
        quad_terms = 0.5 * (np.sum(R**2, axis=1) / sigma2)  # per-sample
        nll_per_sample = const_term + quad_terms  # shape [T-1_w]

        self.p_n = float(np.mean(nll_per_sample))
        # Ensure positivity
        if self.p_n < 0.0:
            self.p_n = 0.0
        return self.p_n

    def _compute_consciousness(self) -> float:
        """Compute consciousness metric C_n.

        C_n = 1 / (1 + exp(- (log(1 + φ_n) + p_n - 0.5)))

        Returns:
            Scalar C_n in (0, 1).
        """
        x = np.log(1.0 + max(0.0, float(self.phi_n))) + float(self.p_n) - 0.5
        # Sigmoid
        self.C_n = float(1.0 / (1.0 + np.exp(-x)))
        return self.C_n

    def _focus_rule(self) -> FocusOn:
        """Generate FocusOn attention directive based on p_n threshold.

        Rule:
            if p_n > 0.5 → FocusOn("visual", "anomaly", 0.9)
            else         → FocusOn("none", "baseline", 0.1)

        Returns:
            FocusOn directive.
        """
        if self.p_n > 0.5:
            return FocusOn(channel="visual", region="anomaly", priority=0.9)
        return FocusOn(channel="none", region="baseline", priority=0.1)

    # -------------------------------------------------------------------------
    # Public API
    # -------------------------------------------------------------------------

    def reset(self) -> None:
        """Reset the observer's internal state and histories."""
        self._Lambda_prev = np.zeros(self.input_dim, dtype=np.float32)
        self._step_count = 0
        self.phi_n = 0.0
        self.p_n = 0.0
        self.C_n = 0.0
        self._z_hist.clear()
        self._phi_scalar_last = 0.0

    def step(self, s_t: np.ndarray) -> Dict[str, object]:
        """Advance the observer by one recursion step with observed state s_t.

        This performs:
            1) Λ[S_t] update (bounded recursion).
            2) Contraction φ(s) and φ_s scalar computation.
            3) VAE compression to latent z_t.
            4) φ_n update via cosine MI approximation (bounded by log(k)).
            5) Surprise p_n via AR predictor on z-history.
            6) Consciousness C_n computation.
            7) FocusOn decision via simple rule on p_n.

        Args:
            s_t: Observed state vector in R^{input_dim}.

        Returns:
            Dictionary with keys:
                - "Lambda_t": np.ndarray, recursive state Λ[S_t]
                - "phi_s": float, scalar contraction ψ used in utility
                - "phi_vec": np.ndarray, contracted vector φ(s)
                - "z_t": np.ndarray, latent vector (128-D)
                - "phi_n": float, integrated information accumulator
                - "p_n": float, surprise metric
                - "C_n": float, consciousness metric
                - "focus": FocusOn, attention directive
                - "step": int, current step index (1-based)
        """
        # 1) Bounded recursion update
        Lambda_t = self._recursion_update(s_t.astype(np.float32))

        # 2) Contraction mapping and scalar φ_s
        phi_vec, phi_s = self._contraction_scalar(Lambda_t)
        self._phi_scalar_last = phi_s

        # 3) VAE compression to latent z_t
        z_np = self.vae.compress(Lambda_t[None, :], use_mean=True)[0]  # [latent_dim,]
        if z_np.shape != (self.latent_dim,):
            raise ValueError(f"VAE compress output must be shape ({self.latent_dim},), got {z_np.shape}")
        self._z_hist.append(z_np.astype(np.float32))

        # Keep only last ar_window entries
        if len(self._z_hist) > self.ar_window:
            self._z_hist = self._z_hist[-self.ar_window :]

        # 4) Update φ_n
        self._update_phi_n(z_np)

        # 5) Surprise p_n
        self._compute_surprise()

        # 6) Consciousness C_n
        self._compute_consciousness()

        # 7) Focus rule
        focus = self._focus_rule()

        return {
            "Lambda_t": Lambda_t.copy(),
            "phi_s": float(self._phi_scalar_last),
            "phi_vec": phi_vec.copy(),
            "z_t": z_np.copy(),
            "phi_n": float(self.phi_n),
            "p_n": float(self.p_n),
            "C_n": float(self.C_n),
            "focus": focus,
            "step": int(self._step_count),
        }

    def run_sequence(self, states: np.ndarray) -> List[Dict[str, object]]:
        """Process a sequence of observed states with bounded recursion.

        Args:
            states: Array of shape [T, input_dim] with observed states.

        Returns:
            List of per-step outputs as returned by step(...).

        Raises:
            ValueError: If states has incorrect shape or exceeds recursion depth.
        """
        if states.ndim != 2 or states.shape[1] != self.input_dim:
            raise ValueError(f"states must have shape [T, {self.input_dim}], got {states.shape}")
        if states.shape[0] > self.recursion_depth:
            raise ValueError(
                f"Sequence length {states.shape[0]} exceeds recursion_depth={self.recursion_depth}."
            )
        outputs: List[Dict[str, object]] = []
        for t in range(states.shape[0]):
            outputs.append(self.step(states[t]))
        return outputs

    def current_metrics(self) -> Dict[str, float]:
        """Return current aggregate metrics (φ_n, p_n, C_n, φ_s)."""
        return {
            "phi_n": float(self.phi_n),
            "p_n": float(self.p_n),
            "C_n": float(self.C_n),
            "phi_s": float(self._phi_scalar_last),
        }


__all__ = ["RecursiveSelfObserver", "FocusOn"]
